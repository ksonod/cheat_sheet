package cheatSheet;

//Example 2: Multiplication			

//"public" means that other classes can access this method
public class ClassExample2 {
		
	// No constructor here in this example. It is different from ClassExample1
	
	
	/* We declare a method. Scope (public/private), return type (int), method name (prod),
	   and parameters (a and b) should be specified.   */
	public int prod(int a, int b)
	{
		return a * b;
	}
}
